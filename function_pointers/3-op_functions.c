/**
 * op_add - returns the sum of a and b
 * @a: The first num
 * @b: The second num
 *
 * Return: The sum of a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns the result of a - b
 * @a: The first num
 * @b: The second num
 *
 * Return: The result of a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns the product of a and b
 * @a: The first num
 * @b: The second num
 *
 * Return: The result of a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns the result of a divided by b
 * @a: The first num
 * @b: The second num
 *
 * Return: The result of a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns the result of a mod b
 * @a: The first num
 * @b: The second num
 *
 * Return: The result of a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
